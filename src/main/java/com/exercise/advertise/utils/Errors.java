package com.exercise.advertise.utils;

import lombok.Data;

@Data
public class Errors {

	String code;

	String message;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

}
